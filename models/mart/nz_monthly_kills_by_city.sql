WITH kills AS (
    SELECT 
        TO_CHAR(STRIKED_DATE::DATE, 'YYYY-MM') AS MONTH_YEAR,
        TOWN_CITY,
        TERRITORIAL_AUTHORITY,
        NUM_KILLS,
        STRIKED_DAY,
        STRIKED_MONTH,
        STRIKED_YEAR
    FROM {{ ref('nz_daily_kills') }}
)
SELECT 
    MONTH_YEAR, 
    TOWN_CITY, 
    TERRITORIAL_AUTHORITY, 
    SUM(NUM_KILLS) AS NUM_KILLS,
    STRIKED_MONTH,
    STRIKED_YEAR
FROM kills 
GROUP BY MONTH_YEAR, 
    TOWN_CITY, 
    TERRITORIAL_AUTHORITY, 
    STRIKED_MONTH,
    STRIKED_YEAR
ORDER BY MONTH_YEAR DESC, NUM_KILLS DESC, TERRITORIAL_AUTHORITY, TOWN_CITY
